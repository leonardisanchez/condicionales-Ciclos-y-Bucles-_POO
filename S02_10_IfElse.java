public class S02_10_IfElse {
    
    
    public static void main(String[] args) {
        int edad = 18;

        // if-else: Verificar si la edad es mayor o igual a 18
        if (edad >= 18) {
            System.out.println("Eres mayor de edad"); // Imprime que es mayor de edad
        } else {
            System.out.println("Eres menor de edad"); // Imprime que es menor de edad
        }
    }
}

    

