public class S02_10_DoWhile {
    
    
    public static void main(String[] args) {
        int i = 1;
        
        // Do-While: Ejecutar al menos una vez y repetir mientras i sea menor o igual que 5
        do {
            System.out.println("El valor de i es: " + i); // Imprime el valor de i
            i++; // Incrementa el valor de i en 1
        } while (i <= 5); // Repite mientras i sea menor o igual que 5
    }
}

    
    

