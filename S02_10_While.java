public class S02_10_While {

    public static void main(String[] args) {
        int i = 1;

        // While: Repetir mientras i sea menor o igual que 5
        while (i <= 5) {
            System.out.println("El valor de i es: " + i); // Imprime el valor de i
            i++; // Incrementa el valor de i en 1
        }
    }
}
